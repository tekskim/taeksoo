import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { globSync } from 'glob';
// lodash 제거: 소규모 유틸 대체
import { generateCSSVarsFromObject } from './token-utils.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

/**
 * 간단한 깊은 병합 유틸
 * - 객체만 지원 (배열은 전체 덮어쓰기)
 * - 원본 객체는 변경하지 않음
 */
function deepMerge(base = {}, override = {}) {
  const result = { ...base };

  for (const [key, value] of Object.entries(override)) {
    const baseValue = result[key];

    if (
      baseValue &&
      typeof baseValue === 'object' &&
      !Array.isArray(baseValue) &&
      value &&
      typeof value === 'object' &&
      !Array.isArray(value)
    ) {
      result[key] = deepMerge(baseValue, value);
    } else {
      result[key] = value;
    }
  }

  return result;
}

/**
 * 토큰 타입별 CSS 섹션 생성
 */
function generateTokenSection(tokens, tokenType, allTokens) {
  if (!tokens[tokenType]) return '';

  return [
    `  /* ${tokenType.charAt(0).toUpperCase() + tokenType.slice(1)} Tokens */`,
    generateCSSVarsFromObject(tokens[tokenType], tokenType, allTokens),
    '',
  ].join('\n');
}

/**
 * 특정 테마에 대한 CSS 생성
 */
function generateThemeCSS(tokens, outputPath, themeName) {
  const header = [
    `/* Auto-generated CSS Custom Properties from tokens/${themeName}.json */`,
    `/* Do not edit this file directly - edit tokens/${themeName}.json instead */`,
    '',
  ].join('\n');

  // 기본 테마는 :root에, 다른 테마는 data attribute 선택자로
  const selector =
    themeName === 'light' ? ':root' : `[data-theme="${themeName}"]`;

  // 토큰 타입들을 순서대로 처리
  const tokenTypes = ['primitive', 'semantic', 'component'];
  const tokenSections = tokenTypes
    .map(tokenType => generateTokenSection(tokens, tokenType, tokens))
    .filter(Boolean);

  const css = [header, `${selector} {`, tokenSections.join(''), '}', ''].join(
    '\n'
  );

  fs.writeFileSync(outputPath, css, 'utf8');
}

/**
 * 모든 토큰을 import하는 index.css 파일 생성
 */
function generateIndexCSS(tokenFiles, outputDir) {
  const indexPath = path.join(outputDir, 'index.css');

  const imports = tokenFiles
    .map(file => {
      const themeName = path.basename(file, '.json');
      return `@import './tokens-${themeName}.css';`;
    })
    .join('\n');

  const css = [
    '/* Auto-generated token imports */',
    '/* Import all theme files */',
    '',
    imports,
    '',
    '/* Theme switching utility */',
    'html[data-theme] {',
    '  color-scheme: var(--color-scheme, light);',
    '}',
    '',
    'html[data-theme="dark"] {',
    '  color-scheme: dark;',
    '}',
    '',
  ].join('\n');

  fs.writeFileSync(indexPath, css, 'utf8');
}

/**
 * 테마 토큰에서 extends 키를 해석하여 최종 토큰 생성
 */
function resolveThemeTokens(themeName, rawTokensMap, cache, stack = new Set()) {
  if (cache.has(themeName)) {
    return cache.get(themeName);
  }

  const rawTokens = rawTokensMap.get(themeName);

  if (!rawTokens) {
    throw new Error(
      `❌ [Token Generator] Missing token definition for theme "${themeName}"`
    );
  }

  if (stack.has(themeName)) {
    throw new Error(
      `❌ [Token Generator] Circular token inheritance detected: ${[
        ...stack,
        themeName,
      ].join(' → ')}`
    );
  }

  stack.add(themeName);

  const { extends: extendField, ...themeTokens } = rawTokens;

  const extendList = Array.isArray(extendField)
    ? extendField
    : extendField
      ? [extendField]
      : [];

  let resolvedTokens = {};

  for (const baseTheme of extendList) {
    const baseTokens = resolveThemeTokens(
      baseTheme,
      rawTokensMap,
      cache,
      stack
    );
    resolvedTokens = deepMerge(resolvedTokens, baseTokens);
  }

  resolvedTokens = deepMerge(resolvedTokens, themeTokens);

  stack.delete(themeName);
  cache.set(themeName, resolvedTokens);

  return resolvedTokens;
}

/**
 * 모든 토큰 파일을 CSS Custom Properties로 변환
 */
function generateCSSTokens() {
  const tokensDir = path.join(__dirname, '../tokens');
  const outputDir = path.join(__dirname, '../src/styles/tokens');

  try {
    // 모든 .json 토큰 파일 찾기
    const tokenFiles = globSync('*.json', { cwd: tokensDir });

    if (tokenFiles.length === 0) {
      console.warn(
        '⚠️ [Token Generator] No token files found in tokens directory'
      );
      return;
    }

    // 출력 디렉토리 생성
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
    }

    const rawTokensMap = new Map(
      tokenFiles.map(file => {
        const themeName = path.basename(file, '.json');
        const tokensPath = path.join(tokensDir, file);
        const tokens = JSON.parse(fs.readFileSync(tokensPath, 'utf8'));
        return [themeName, tokens];
      })
    );

    const resolvedCache = new Map();

    // 각 토큰 파일에 대해 CSS 생성 (함수형 방식)
    const processedThemes = tokenFiles.map(file => {
      const themeName = path.basename(file, '.json');
      const outputPath = path.join(outputDir, `tokens-${themeName}.css`);
      const resolvedTokens = resolveThemeTokens(
        themeName,
        rawTokensMap,
        resolvedCache
      );
      generateThemeCSS(resolvedTokens, outputPath, themeName);
      return themeName;
    });

    // 기본 import 파일 생성
    generateIndexCSS(tokenFiles, outputDir);

    console.log(
      `✅ [Token Generator] Generated CSS tokens for ${tokenFiles.length} themes: ${processedThemes.join(
        ', '
      )}`
    );
  } catch (error) {
    console.error(
      '❌ [Token Generator] Error generating CSS tokens:',
      error.message
    );
    process.exit(1);
  }
}

// 스크립트 실행
generateCSSTokens();
